export const chats = conversations;

// Helper to get user by ID
export const getUserById = (id) => {
  if (id === "current") return currentUser;
  return users.find((user) => user._id === id);
};

// Helper to get chat partner (other participant)
export const getChatPartner = (conversation) => {
  const partnerId = conversation.participants.find((id) => id !== "current");
  return getUserById(partnerId);
};

// Helper to get last message object for a conversation
export const getLastMessage = (conversation) => {
  const convMessages = messages[conversation._id];
  if (!convMessages || convMessages.length === 0) return null;
  return convMessages.find((m) => m._id === conversation.lastMessage) || convMessages[convMessages.length - 1];
};
